def sort(args):
    """sort lines of text files"""

    lines_to_sort = []

    for file in args:
        file = open(file)

        for line in file:
            lines_to_sort.append(line)

        file.close()

    lines_to_sort = sorted(lines_to_sort)

    for line in lines_to_sort:
        print(line, end="")
